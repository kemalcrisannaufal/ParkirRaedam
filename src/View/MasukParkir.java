    /**
     * This method is called when the nomorKendaraanTextField gains focus.
     */
    private void nomorKendaraanTextFieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nomorKendaraanTextFieldFocusGained
        if (nomorKendaraanTextField.getText().equals("Masukkan Plat")){
            nomorKendaraanTextField.setText(null);
            nomorKendaraanTextField.requestFocus();
            Komponen.removePlaceholderStyleJ(nomorKendaraanTextField);
        }
    }//GEN-LAST:event_nomorKendaraanTextFieldFocusGained

    /**
     * This method is called when nomorKendaraanTextField loses focus.
     */
    private void nomorKendaraanTextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nomorKendaraanTextFieldFocusLost
        if (nomorKendaraanTextField.getText().length() == 0){
            Komponen.addPlaceholderStyleJ(nomorKendaraanTextField);
            nomorKendaraanTextField.setText("Masukkan Plat");
        } 
    }//GEN-LAST:event_nomorKendaraanTextFieldFocusLost

