    /**
     * The simpanButtonActionPerformed method is triggered when the user clicks the "Simpan" button.
     * Retrieves the vehicle plate number from the nomorKendaraanTextField.
     * Checks if jenisKendaraan, nomorKendaraan, and tipeParkir have been selected or filled.
     * Verifies the existence of parking settings (pengaturanC.getPengaturanParkir()).
     * Checks if there is available parking space for the selected vehicle type.
     * @param evt 
     */
    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        String nomorKendaraan = nomorKendaraanTextField.getText();
        if (!nomorKendaraan.isEmpty() && !jenisKendaraan.isEmpty() && !tipeParkir.isEmpty()){
            if (pengaturanC.getPengaturanParkir()!=null) {
                if (parkirC.searchByPlateNum(nomorKendaraan) == null) {
                    if (parkirC.isParkirKosong(jenisKendaraan)) {
                    Parkir parkir = parkirC.createObjekMasukParkir(nomorKendaraan, jenisKendaraan, tipeParkir);
                    if (parkir instanceof ParkirPerJam){
                        ((ParkirPerJam) parkir).masukParkir(tipeParkir);
                        parkirC.insertParkir(parkir);
                    } else {
                        ((ParkirInap) parkir).masukParkir(tipeParkir);
                        parkirC.insertParkir(parkir);
                    }
                    JOptionPane.showMessageDialog(this, "Data parkir berhasil ditambahkan");
                    new Dashboard().setVisible(true);
                    dispose();
                    } else {
                        JOptionPane.showMessageDialog(MasukParkir.this, "Parkir Penuh!!");
                    }
                } else {
                    JOptionPane.showMessageDialog(MasukParkir.this, "Plat nomor telah terdaftar sebagai kendaraan yang sedang parkir");
                }
            } else {
                JOptionPane.showMessageDialog(MasukParkir.this, "Mohon Isi Pengaturan Parkir Terlebih Dahulu");
            }
            
        } else {
            JOptionPane.showMessageDialog(MasukParkir.this, "Mohon isi semua data yang diperlukan!");
        }
    }//GEN-LAST:event_simpanButtonActionPerformed

